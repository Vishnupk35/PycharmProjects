#OOPS Concept

#3 Basic concepts. (***Important)

#1. Class: General structure or it's a basic design model. We can create multiple objects in a class.
#2. Object: The thing which created or developed with the class is called object. (Real world entity)
#3. Reference: Operations runs externally to the object (ex: f=open, here f is the reference)

